# Operators

a, b = [2, 3]

# Arithmetic Operators

a + b  # Addition
a - b  # Substraction
a * b  # Multiplication
a / b  # Division
a % b  # Modulus
a ** b  # Exponentiation
a // b  # Floor division

# Assignment Operators

a = 5     # a = 5
a += 3    # a = a + 3
a -= 3    # a = a - 3
a *= 3    # a = a * 3
a /= 3    # a = a / 3
a %= 3    # a = a % 3
a //= 3   # a = a // 3
a **= 3   # a = a ** 3
a &= 3    # a = a & 3
a |= 3    # a = a | 3
a ^= 3    # a = a ^ 3
a >>= 3   # a = a >> 3
a <<= 3   # a = a << 3

# Comparison Operators

a == b
a != b
a > b
a < b
a >= b
a <= b

# Logical Operators

a < 5 and a < 10        # and
a < 5 or a < 10         # or
not(a < 5 and a < 10)   # not

# Indentity Operators

a is b      # True/False
a is not b  # True/False

# Membership Operators

a in b      # True/False
a not in b  # True/False

