# Python comments

# In Python comments starts with a "#" sign.
# Everything after "#" Python will ignore.

# One Line Comment

# print("Hello World!")
print("Hello World!")  # this is a comment

# Multi Line Comments

# This is a comment
# written in
# more than just one line.

"""
This is a comment
written in
more than just one line.
"""

# As long as the string is not assigned to a variable
# Python will read the code, but than ignore it.
